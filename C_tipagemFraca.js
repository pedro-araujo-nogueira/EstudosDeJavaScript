// javaScript é uma linguagem de tipagem fraca, ou seja, tem uma tipagem dinâmica.
// O fato de uma linguagem ser fracamente tipada não significa que ela não tem tipo, 
// significa apenas que uma variavel pode assumir tipos diferentes.

let qualquer = 'legal'
console.log(qualquer)
console.log(typeof qualquer)

qualquer = 3.1516
